# 1. Lists - A collection of items in order
friends = ["Alice", "Bob", "Charlie"]
print("My friends are:", friends)
print("The first friend is:", friends[0])

# 2. Dictionaries - Store data with labels (Keys)
user = {
    "name": "Alex",
    "age": 25,
    "city": "Nairobi"
}
print("User Profile:", user)
print("User Name:", user["name"])

